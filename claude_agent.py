async def chat_with_kolya(user_id: int, user_message: str) -> str:
    raw_history = await get_chat_history(user_id, limit=20)

    # Ensure alternating user/assistant
    clean_history = []
    expected_role = "user"
    for msg in raw_history:
        if msg["role"] == expected_role:
            clean_history.append({"role": msg["role"], "content": msg["content"]})
            expected_role = "assistant" if expected_role == "user" else "user"

    messages = clean_history + [{"role": "user", "content": user_message}]

    try:
        response = await client.messages.create(
            model="claude-sonnet-4-6",
            max_tokens=1024,
            system=SYSTEM_PROMPT,
            messages=messages
        )
        result_text = response.content[0].text if response.content else "Зрозумів!"
    except Exception as e:
        result_text = f"Помилка: {str(e)[:200]}"

    await save_message(user_id, "user", user_message)
    await save_message(user_id, "assistant", result_text)

    return result_text
